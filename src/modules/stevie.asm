* tv.init
* Initialize main editor
***************************************************************
* bl @stevie.init
*--------------------------------------------------------------
* INPUT
* none
*--------------------------------------------------------------
* OUTPUT
* none
*--------------------------------------------------------------
* Register usage
* tmp0
*--------------------------------------------------------------
* Notes
***************************************************************
stevie.init:
        dect  stack
        mov   r11,*stack            ; Save return address
        ;------------------------------------------------------
        ; Initialize
        ;------------------------------------------------------
        clr   @tv.colorscheme       ; Set default color scheme        
        ;------------------------------------------------------
        ; Exit
        ;------------------------------------------------------
stevie.init.exit:        
        b     @poprt                ; Return to caller
