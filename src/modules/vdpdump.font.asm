* FILE......: vdpdump.font.asm
* Purpose...: Dump sprite/character patterns to VDP

***************************************************************
* vdpdump.patterns
* Dump Stevie font to VDP
***************************************************************
* bl @vdpdump.font
*--------------------------------------------------------------
* INPUT
* @parm1 = Pointer to font in ROM
*--------------------------------------------------------------
* OUTPUT
* None
*--------------------------------------------------------------
* REMARK
* None
*--------------------------------------------------------------
* Register usage
* r11
***************************************************************
vdp.dump.font:
        dect  stack
        mov   r11,*stack            ; Push return address
        ;-------------------------------------------------------
        ; Dump from ROM to VDP PDT
        ;-------------------------------------------------------
        bl    @cpym2v
              data fntadr - 16,>6200,96*8
        ;-------------------------------------------------------
        ; Exit
        ;-------------------------------------------------------
vdp.dump.font.exit:
        mov   *stack+,r11           ; Pop R11
        b     *r11                  ; Return to task
