* basic......: fb.cursor.on.asm
* Purpose....: Turn cursor on

***************************************************************
* fb.cursor.on
* Turn cursor on
***************************************************************
* bl @fb.cursor.on
*--------------------------------------------------------------
* INPUT
* none
*--------------------------------------------------------------
* OUTPUT
* none
*--------------------------------------------------------------
* Register usage
* tmp0,tmp1,tmp2
*--------------------------------------------------------------
* Notes
********|*****|*********************|**************************
fb.cursor.on:
        dect  stack
        mov   r11,*stack            ; Save return address
        dect  stack
        mov   tmp0,*stack           ; Push tmp0
        ;-------------------------------------------------------
        ; Turn cursor on
        ;-------------------------------------------------------
        clr   @fb.curtoggle         ; Turn cursor on
        
        ;-------------------------------------------------------
        ; Exit
        ;-------------------------------------------------------
fb.cursor.on.exit:
        mov   *stack+,tmp0          ; Pop tmp0
        mov   *stack+,r11           ; Pop R11
        b     *r11                  ; Return to caller        
