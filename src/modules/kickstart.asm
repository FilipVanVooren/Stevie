* FILE......: kickstart.asm
* Purpose...: Bankswitch routine for starting stevie

***************************************************************
* stevie Cartridge Header & kickstart ROM bank 0
***************************************************************
* 
*--------------------------------------------------------------
* INPUT
* none
*--------------------------------------------------------------
* OUTPUT
* none
*--------------------------------------------------------------
* Register usage
* r0
***************************************************************

*--------------------------------------------------------------
* Cartridge header
********|*****|*********************|**************************
        byte  >aa,1,1,0,0,0
        data  $+10
        byte  0,0,0,0,0,0,0,0
        data  0                     ; No more items following
        data  kickstart.code1

        .ifdef debug
              #string 'STEVIE %%build_date%%'
        .else
              #string 'STEVIE'
        .endif         

*--------------------------------------------------------------
* Kickstart bank 0
********|*****|*********************|**************************
        aorg  kickstart.code1
        clr   @>6000                ; Switch to bank 0
